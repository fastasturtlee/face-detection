import tkinter as tk
from tkinter import font  as tkfont
import detector as t
import dataset as d
import trainner as r

class SampleApp(tk.Tk):

    def __init__(self, *args, **kwargs):
        tk.Tk.__init__(self, *args, **kwargs)

        self.title_font = tkfont.Font(family='Helvetica', size=18, weight="bold", slant="italic")

        container = tk.Frame(self)
        container.pack(side="top", fill="both", expand=True)
        container.grid_rowconfigure(0, weight=1)
        container.grid_columnconfigure(0, weight=1)

        self.frames = {}
        for F in (StartPage, PageOne, PageTwo,PageThree):
            page_name = F.__name__
            frame = F(parent=container, controller=self)
            self.frames[page_name] = frame
            frame.grid(row=0, column=0, sticky="nsew")
        self.show_frame("StartPage")

    def show_frame(self, page_name):
        frame = self.frames[page_name]
        frame.tkraise()


class StartPage(tk.Frame):

    def __init__(self, parent, controller):
        tk.Frame.__init__(self, parent)
        self.controller = controller
        label = tk.Label(self, text="welcome to smart cctv system", font=controller.title_font)
        label.pack(side="top", fill="x", pady=10)
        label1 = tk.Label(self,text= "to exit from monitor press q", font =("courier",10),fg = "red")
        label1.pack(side = "top",fill ="x",pady = 10)

        button1 = tk.Button(self, text="monitor",
                            command=lambda: t.monitor())
        button2 = tk.Button(self, text="capture image of person",
                            command=lambda: d.capture())
        button3 = tk.Button(self, text="Update the system",
                            command=lambda: r.update())
        button1.pack()
        button2.pack()
        button3.pack()


class PageOne(tk.Frame):

    def __init__(self, parent, controller):
        tk.Frame.__init__(self, parent)
        self.controller = controller
